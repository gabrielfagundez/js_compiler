// Pruebas para ToUpperCase y ToLowerCase

console.log("AAA".toUpperCase());
console.log("AA BB CC".toUpperCase());
console.log("A B C".toUpperCase());
console.log("aaa".toUpperCase());
console.log("a".toUpperCase());
console.log("aa bb cc".toUpperCase());

console.log("AAA".toLowerCase());
console.log("AA BB CC".toLowerCase());
console.log("A B C".toLowerCase());
console.log("aaa".toLowerCase());
console.log("a".toLowerCase());
console.log("aa bb cc".toLowerCase());
